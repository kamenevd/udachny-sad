/**
 * SkipLink — скрытая ссылка «Перейти к содержимому» для скринридеров (задача 19.1).
 *
 * При Tab-фокусе появляется сверху экрана, позволяет клавиатурным
 * пользователям пропустить навигацию и сразу попасть к основному контенту.
 *
 * Использование: разместить в начале каждого экрана с id="main-content".
 *   <SkipLink />
 *   <main id="main-content">...</main>
 */

export function SkipLink() {
  return (
    <a href="#main-content" className="skip-link">
      Перейти к содержимому
    </a>
  );
}
